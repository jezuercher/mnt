function col = sunrise_colors(n)

% function col = sunrise_colors(n)

eval(default('n','250'));

c = [...
   0,0,205;...
   0,0,245;...
   15,100,255;...
   30,144,255;...
   95,184,255;...
   125,215,255;...
   152,245,255;...
   187,255,255;...
   240,255,210;...
   255,246,143;...
   255,225,80;...
   255,193,37;...
   255,160,20;...
   255,120,15;...
   255,69,10;...
   245,20,5;...
   220,20,5;...
    ]/256;

nn  = size(c,1);
col = interp1(1:nn,c,1:[nn-1]/(n-1):nn,'cubic');

col = flipud(col);

if n==1,
  col = [0 0 1];
end
